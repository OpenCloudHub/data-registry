#!/usr/bin/env bash
set -e

# ==============================================================================
# Run all data pipelines and create versioned tags
# ==============================================================================

BUMP_TYPE="${BUMP_TYPE:-patch}"
IS_CRON="${IS_CRON:-false}"

echo ""
echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"
echo "üöÄ Data Registry Pipeline Execution"
echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"
echo "Bump type: ${BUMP_TYPE}"
echo "Is cron: ${IS_CRON}"
echo ""

# ------------------------------------------------------------------------------
# Run all pipelines
# ------------------------------------------------------------------------------
echo "üìä Running data processing pipelines..."
echo "‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ"

PIPELINES=(
  "emotion"
  "fashion-mnist"
  "wine-quality"
  "opencloudhub-readmes-download"
)

for pipeline in "${PIPELINES[@]}"; do
  echo "  ‚Üí Processing ${pipeline}..."
  dvc repro pipelines/${pipeline}/dvc.yaml
  echo "     ‚úì ${pipeline} complete"
done

echo "  ‚Üí Adding RAG evaluation questions..."
dvc add data/opencloudhub-readmes/rag-evaluation/questions.csv 2>/dev/null || echo "     ‚ÑπÔ∏è  Already tracked"
echo "     ‚úì RAG evaluation questions processed"

echo ""

# ------------------------------------------------------------------------------
# Detect which datasets changed using dvc diff
# ------------------------------------------------------------------------------
echo "üîç Detecting changed datasets..."
echo "‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ"

DVC_DIFF=$(dvc diff --json HEAD 2>/dev/null || echo '{"added":[],"deleted":[],"modified":[],"renamed":[]}')

CHANGED_DATASETS=$(echo "$DVC_DIFF" | python3 -c "
import sys, json
try:
    diff = json.load(sys.stdin)
    datasets = set()
    
    for change_type in ['added', 'modified']:
        for item in diff.get(change_type, []):
            path = item.get('path', '')
            if path.startswith('data/'):
                parts = path.split('/')
                if len(parts) >= 2:
                    datasets.add(parts[1])
    
    for item in diff.get('renamed', []):
        new_path = item.get('path', {}).get('new', '')
        if new_path.startswith('data/'):
            parts = new_path.split('/')
            if len(parts) >= 2:
                datasets.add(parts[1])
    
    for ds in sorted(datasets):
        print(ds)
except:
    pass
")

if [ -z "$CHANGED_DATASETS" ]; then
  echo "  ‚ÑπÔ∏è  No datasets changed - skipping push and tagging"
  exit 0
fi

# Show what changed
while IFS= read -r dataset; do
  [ -n "$dataset" ] && echo "  ‚úì Changes detected: ${dataset}"
done <<< "$CHANGED_DATASETS"

echo ""

# ------------------------------------------------------------------------------
# Push data to remote storage
# ------------------------------------------------------------------------------
echo "üì§ Pushing data to remote storage..."
echo "‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ"
dvc push
echo "   ‚úì Data pushed to MinIO"
echo ""

# ------------------------------------------------------------------------------
# Create tags only for changed datasets
# ------------------------------------------------------------------------------
echo "üè∑Ô∏è  Creating tags for changed datasets..."
echo "‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ"

while IFS= read -r dataset; do
  [ -z "$dataset" ] && continue
  
  LATEST_TAG=$(git tag -l "${dataset}-v*" --sort=-version:refname | head -n1)
  
  if [ -z "$LATEST_TAG" ]; then
    NEW_VERSION="1.0.0"
  else
    CURRENT_VERSION=$(echo "$LATEST_TAG" | grep -oP "${dataset}-v\K[0-9]+\.[0-9]+\.[0-9]+" | head -1)
    IFS='.' read -r MAJOR MINOR PATCH <<< "$CURRENT_VERSION"
    
    case $BUMP_TYPE in
      major) NEW_VERSION="$((MAJOR + 1)).0.0" ;;
      minor) NEW_VERSION="${MAJOR}.$((MINOR + 1)).0" ;;
      patch) NEW_VERSION="${MAJOR}.${MINOR}.$((PATCH + 1))" ;;
    esac
  fi
  
  if [ "$IS_CRON" = "true" ]; then
    TAG_NAME="${dataset}-v${NEW_VERSION}-automated"
    TAG_MESSAGE="${dataset} v${NEW_VERSION} (automated)"
  else
    TAG_NAME="${dataset}-v${NEW_VERSION}"
    TAG_MESSAGE="${dataset} v${NEW_VERSION}"
  fi
  
  git tag -a "${TAG_NAME}" -m "${TAG_MESSAGE}"
  echo "   ‚úì Created: ${TAG_NAME}"
done <<< "$CHANGED_DATASETS"

echo ""

# ------------------------------------------------------------------------------
# Summary
# ------------------------------------------------------------------------------
echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"
echo "‚úÖ Pipeline execution complete!"
echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"
echo ""
echo "üì¶ Tagged datasets:"
while IFS= read -r dataset; do
  [ -n "$dataset" ] && {
    LATEST_TAG=$(git tag -l "${dataset}-v*" --sort=-version:refname | head -n1)
    echo "   ‚Ä¢ ${LATEST_TAG}"
  }
done <<< "$CHANGED_DATASETS"
echo ""